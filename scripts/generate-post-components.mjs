import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const currentFile = fileURLToPath(import.meta.url);
const scriptsDir = path.dirname(currentFile);
const repoRoot = path.resolve(scriptsDir, '..');
const postsDir = path.join(repoRoot, 'src', 'content', 'posts');
const outputPath = path.join(repoRoot, 'src', 'lib', 'content', 'post-components.generated.ts');

const mode = process.env.POST_COMPONENT_MAP_MODE === 'dev' ? 'dev' : 'prod';

function extractFrontmatter(source, filePath) {
	const match = source.match(/^---\r?\n([\s\S]*?)\r?\n---(?:\r?\n|$)/);
	if (!match) {
		throw new Error(`Missing frontmatter block in ${filePath}`);
	}
	return match[1];
}

function parseBoolean(rawValue, key, filePath) {
	const normalized = rawValue.trim().replace(/^['"]|['"]$/g, '').toLowerCase();
	if (normalized === 'true') return true;
	if (normalized === 'false') return false;
	throw new Error(`Invalid boolean for "${key}" in ${filePath}: ${rawValue}`);
}

function parseDevOnlyFlag(frontmatter, filePath) {
	const match = frontmatter.match(/^\s*devOnly\s*:\s*([^\r\n#]+)/m);
	if (!match) return undefined;
	return parseBoolean(match[1], 'devOnly', filePath);
}

function toSlug(filename) {
	return filename.replace(/\.md$/u, '');
}

function buildGeneratedSource(posts) {
	const lines = [];
	lines.push('// This file is generated by scripts/generate-post-components.mjs');
	lines.push(`// POST_COMPONENT_MAP_MODE=${mode}`);
	lines.push("import type { Component } from 'svelte';");
	lines.push('');
	lines.push('type PostComponentModule = {');
	lines.push('\tdefault: Component;');
	lines.push('};');
	lines.push('');
	lines.push('export const generatedPostComponentSlugs = [');
	posts.forEach((post) => {
		lines.push(`\t'${post.slug}',`);
	});
	lines.push('] as const;');
	lines.push('');
	lines.push('const componentModules = import.meta.glob<PostComponentModule>(');
	lines.push('\t[');
	posts.forEach((post) => {
		lines.push(`\t\t'/src/content/posts/${post.filename}',`);
	});
	lines.push('\t],');
	lines.push('\t{ eager: true }');
	lines.push(');');
	lines.push('');
	lines.push('const componentMap = new Map(');
	lines.push('\tObject.entries(componentModules).map(([path, module]) => {');
	lines.push("\t\tconst filename = path.split('/').at(-1) ?? '';");
	lines.push("\t\tconst slug = filename.replace('.md', '');");
	lines.push('\t\treturn [slug, module.default] as const;');
	lines.push('\t})');
	lines.push(');');
	lines.push('');
	lines.push('export function getPostComponentBySlug(slug: string) {');
	lines.push('\treturn componentMap.get(slug);');
	lines.push('}');
	lines.push('');

	return `${lines.join('\n')}`;
}

async function run() {
	const files = (await readdir(postsDir)).filter((file) => file.endsWith('.md')).sort();
	const records = await Promise.all(
		files.map(async (filename) => {
			const filePath = path.join(postsDir, filename);
			const source = await readFile(filePath, 'utf8');
			const frontmatter = extractFrontmatter(source, filePath);
			return {
				filename,
				slug: toSlug(filename),
				devOnly: parseDevOnlyFlag(frontmatter, filePath)
			};
		})
	);

	const includedPosts =
		mode === 'dev' ? records : records.filter((record) => record.devOnly !== true);
	const source = buildGeneratedSource(includedPosts);
	await writeFile(outputPath, source, 'utf8');

	console.log(
		`Generated ${path.relative(repoRoot, outputPath)} with ${includedPosts.length} post components (${mode} mode).`
	);
}

run().catch((error) => {
	console.error(error instanceof Error ? error.message : 'Failed to generate post components.');
	process.exit(1);
});
